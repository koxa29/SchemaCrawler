SELECT /*+ PARALLEL(AUTO) */
  NULL AS SEQUENCE_CATALOG,
  SEQUENCE_OWNER AS SEQUENCE_SCHEMA,
  SEQUENCE_NAME AS SEQUENCE_NAME,
  INCREMENT_BY AS "INCREMENT",
  MIN_VALUE AS MINIMUM_VALUE,
  MAX_VALUE AS MAXIMUM_VALUE,
  CASE WHEN CYCLE_FLAG = 'Y' THEN 'YES' ELSE 'NO' END AS CYCLE_OPTION,
  ORDER_FLAG,
  CACHE_SIZE,
  LAST_NUMBER
FROM
  ALL_SEQUENCES
WHERE
  SEQUENCE_OWNER NOT IN ('ANONYMOUS','APEX_PUBLIC_USER','CTXSYS','DBSNMP','DIP','EXFSYS','FLOWS_%','FLOWS_FILES','LBACSYS','MDDATA','MDSYS','MGMT_VIEW','OLAPSYS','ORACLE_OCM','ORDDATA','ORDPLUGINS','ORDSYS','OUTLN','OWBSYS','SI_INFORMTN_SCHEMA','SPATIAL_CSW_ADMIN_USR','SPATIAL_WFS_ADMIN_USR','SYS','SYSMAN','SYSTEM','WKPROXY','WKSYS','WK_TEST','WMSYS','XDB','XS$NULL')
  AND SEQUENCE_OWNER NOT LIKE 'APEX%'
ORDER BY
  SEQUENCE_OWNER,
  SEQUENCE_NAME
